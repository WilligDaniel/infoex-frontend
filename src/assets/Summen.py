def calculate_sum_rules(field_values, sum_rule):

    #for key, value in field_values.items():
    #    print(key + " --> " + str(value))

    for field, rule in sorted(sum_rule.items()):
        #print("Field: " + field + " --> Rule: " + rule)
        y = 0
        operator = '+'
        for x in rule.split(' '):
            if x == '+': operator = '+'
            elif x == '-': operator = '-'
            elif x == '*': operator = '*'
            else:
                if x in field_values:
                    if operator == '+': y += float(field_values[x])
                    elif operator == '-': y -= float(field_values[x])
                    elif operator == '*': y *= float(field_values[x])
                    #print(x, field_values[x], y)    
        field_values[field] = y
        #print("Field: " + field + " --> Value: " + str(y))

    #for key, value in field_values.items():
    #    print(key + " --> " + str(value))
        
    return field_values